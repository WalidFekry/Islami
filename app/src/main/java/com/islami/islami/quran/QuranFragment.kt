    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        rec = view.findViewById(R.id.rec_quran)
        adapter = QuranAdapter(ArSuras.toList());
        rec.adapter = adapter
        adapter.onItemSuraClick = object :QuranAdapter.OnItemClickLister{
            override fun onClick(pos: Int, list: List<String>) {
                val intent = Intent(context,SuraContent::class.java)
                intent.putExtra(Constants().suraId,pos)
                intent.putExtra(Constants().suraName,list.get(pos))
                startActivity(intent)
            }
        }
    }


